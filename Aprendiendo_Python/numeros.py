#OPERACIONES MATEMATICAS SIMPLES

#RESTA

a = 10 - 4
print(f"El resultado de la resta es:{a} ")

#SUMA 

b = 5 + 5
print(f"El resultado de la suma es: {b}")

#MULTIPLICACION

c = 2 * 2
print(f"El resultado de la multiplicación es: {c}")

#DIVISION

d = 4 / 2
