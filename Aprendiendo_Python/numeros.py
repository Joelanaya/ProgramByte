#OPERACIONES MATEMATICAS AVANZADAS

#MODULUS 

#SOBRA

m = 7 % 4
print(f"La sobra de 7 % 4 es: {m}")

#VER SI EL NUMERO ES DIVISIBLE 

md = 50 % 5
print(f"50 % 5 = {md}, Es divisible") 

#PAR O IMPAR 

mP = 23 % 1
print(f"El resultado de 23 % 1 es: {mP}, Es par") #PAR, SI EL RESULTADO ES 0 SIGNIFICA QUE ES PAR

mI = 23 % 2
print(f"El resultado de 23 % 2 es: {mI}, Es impar") # IMPAR, SI EL RESULTADO ES 1 SIGNIFICA QUE ES IMPAR

mP2 = 20 % 2
print(f"El resultado de 20 % 2 es: {mP2}, Es par") # PAR



#POTENCIA 

p = 20 ** 2 
print(f"La potencia de 20 ** 2 es: {p}")

p2 = 10 ** 2 
print(f"La potencia de 10 ** 2 es: {p2}")


